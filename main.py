        if form.new_loc.data and form.new_loc.data != loc_on_record.shop_loc:
            loc_on_record.shop_loc = form.new_loc.data
            changes_made = False

        if not changes_made:
            db.session.commit()
            flash("Amendments saved successfully!")
        else:
            flash("No changes detected, please check your changes and try again.")

