@app.route("/login", methods=["GET", "POST"])
def login_page():
    form = LoginForm()
    if form.validate_on_submit():
        password = form.password.data
        results = db.session.execute(db.select(Users).where(Users.email == form.email.data))
        user = results.scalar()
        if not user:
            flash("That email does not exist, please try again or register.")
            return redirect(url_for("login_page"))
        elif not check_password_hash(user.password, password):
            flash("Incorrect password.")
            return redirect(url_for("login_page"))
        else:
            login_user(user)
            session.permanent = True
            return redirect(url_for("dashboard"))

    return render_template("login.html", form=form)

@app.route("/register", methods=["GET", "POST"])
def register_user():
    form = RegisterForm()
    if form.validate_on_submit():
        result = db.session.execute(db.select(Users).where(Users.email == form.email.data))
        user = result.scalar()

        if user:
            flash("This email is linked to an existing account, please sign in!")
            return redirect(url_for("login_page"))

        #Create password hash:
        hash_and_salted_password = generate_password_hash(
            form.password.data,
            method='pbkdf2:sha256',
            salt_length=8,
        )
        new_user = Users(
            username=form.username.data,
            email=form.email.data,
            password=hash_and_salted_password
        )
        db.session.add(new_user)
        db.session.commit()
        #Authentication of the user with Flask-login:
        login_user(new_user)
        return redirect(url_for("dashboard"))
    return render_template("register.html", form=form, current_user=current_user)

