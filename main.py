        if selected_shop != 'all':
            shop_locations = ShopLoc.query.filter_by(shop_id=int(selected_shop)).all()
            location_ids = [loc.id for loc in shop_locations]
            query = query.filter(ItemData.loc_id.in_(location_ids))

        # Now an 'if' for the location
